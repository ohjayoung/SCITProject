CREATE TABLE T_REQUEST
(
   REQNUM number NOT NULL,
   REQUESTER varchar2(20) NOT NULL,
   REQACCEPTER varchar2(20) NOT NULL,
   ISACCEPTED number(4) DEFAULT 0,
